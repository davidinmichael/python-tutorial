# Variables
# In Python, a variable is a name that represents a value. Variables can hold different types of data, such as numbers, strings, and lists.

# Declaring Variables
# To declare a variable in Python, you simply assign a value to a name using the equals sign (=). Here's an example:
message = "Hello, world!"
# In this example, we've declared a variable called message and assigned it the value "Hello, world!".

# Naming Conventions
# When choosing variable names, there are a few naming conventions to keep in mind:

# Variable names can contain letters, numbers, and underscores, but cannot start with a number.
# Variable names should be descriptive and meaningful, so that it's clear what the variable represents.
# By convention, variable names in Python are written in lowercase, with words separated by underscores.

# Data Types
# Variables in Python can hold different types of data. Some common data types include:

# Strings: A sequence of characters, enclosed in quotes (either single quotes or double quotes).
# Numbers: Can be integers (whole numbers) or floats (numbers with decimal points).
# Booleans: Can be True or False.
# Lists: A collection of values, separated by commas and enclosed in square brackets.
# Here's an example of declaring variables with different data types:

# String variable
message = "Hello, world!"

# Integer variable
count = 42

# Float variable
price = 4.99

# Boolean variable
is_active = True # Not true, it is case sensitive
not_active = False # Not true, it is case sensitive

# List variable
fruits = ["apple", "banana", "orange"]

# Accessing Variables
# Once you've declared a variable, you can access its value by simply referring to its name. For example, to print the
# value of the message variable from the previous example, you can use the print() function:
print(message)

# Updating Variables
# You can also update the value of a variable by assigning it a new value. For example, to update the value of the count
# variable from the previous example, you can do:
count = 43
# This will change the value of count from 42 to 43.

# Conclusion
# Variables are a fundamental concept in programming, and understanding how to use them is essential for writing effective code
# in Python. By declaring and manipulating variables, you can store and work with different types of data in your programs.



